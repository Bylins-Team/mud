template<typename T>
struct LoadedEntity {
	int vnum;
	std::unique_ptr<T> data;
	std::string error;
	bool success;

	LoadedEntity() : vnum(-1), data(nullptr), success(false) {}
	LoadedEntity(int v, T* d) : vnum(v), data(d), success(d != nullptr) {}
	LoadedEntity(int v, const std::string& err) : vnum(v), data(nullptr), error(err), success(false) {}
};

